# Clase Tipo_Categoria que representa la categoría de un libro
class Tipo_Categoria():
    # Constructor que inicializa los atributos de la categoría
    def __init__(self, id_tipo_categoria, tipo_categoria):
        self.id_tipo_categoria = id_tipo_categoria  # ID de la categoría
        self.tipo_categoria = tipo_categoria  # Nombre de la categoría
